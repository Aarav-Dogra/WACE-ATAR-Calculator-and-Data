def get_subject_value(data, percentile):
    """
    data = list of (x, y)
    percentile = decimal like 0.35

    Returns:
        total_n
        target_rank = percentile * total_n
        best_index_1_based
        best_x
        best_cumulative
        difference
    """
    total_n = sum(y for x, y in data)
    target_rank = percentile * total_n

    cumulative = 0
    best_index = None
    best_x = None
    best_cumulative = None
    best_diff = float("inf")

    for i, (x, y) in enumerate(data, start=1):
        cumulative += y
        diff = abs(cumulative - target_rank)

        if diff < best_diff:
            best_diff = diff
            best_index = i
            best_x = x
            best_cumulative = cumulative

    return total_n, target_rank, best_index, best_x, best_cumulative, best_diff


