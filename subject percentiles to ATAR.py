def atar_below_97(x): 
    u=(x-177.755)/49.566
    return 100 / (1 + math.exp(-(u + 0.133804*u*u - 0.0143201*u*u*u)))


def interpolate_atar(tea, data):
    data = sorted(data, key=lambda t: t[0])

    if tea < data[0][0]:
        return atar_below_97(tea)
    if tea > data[-1][0]:
        return data[-1][1]

    for (x1, y1), (x2, y2) in zip(data, data[1:]):
        if x1 <= tea <= x2:
            atar = y1 + (tea - x1) * (y2 - y1) / (x2 - x1)
            if atar < 97:
                return atar_below_97(tea)
            return atar


